void main() {
  List<String> names = ["Shupto", "Anjim", "Shajib"];
  List<num> numbers = [10, 20, 24, 30];
  print("Value of names is $names");
  print("Value of name [0] is ${names[0]}"); //index 0
  print("Value of name [1] is ${names[1]}"); //index 1
  print("Value of name [2] is ${names[2]}"); //index 2
  print("\n");
  print("Value of numbers is $numbers");
  print("Value of number [0] is ${numbers[0]}");
  print("Value of number [1] is ${numbers[1]}");
  print("Value of number [2] is ${numbers[2]}");
  print(names.length);
  print(numbers.length);
}
